import SwiftUI

struct BrandGridView: View {
    var body: some View {
        ScrollView(.horizontal, showsIndicators: false) {
            LazyHGrid(rows: gridLayout, spacing: columnSpacing) {
                ForEach(brands) { item in
                    BrandItemView(model: item)
                }
            }
            .frame(height: 200)
            .padding(15)
        }
    }
}

